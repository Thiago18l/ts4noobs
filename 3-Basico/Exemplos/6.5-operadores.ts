// OPERADORES BITWISE

let result;

// Operador AND
result = (2 & 3) // 
console.log(result) // 2

//Operador OR
result = ( 2 | 3 )
console.log(result) // 3

//Operador XOR
result = (2 ^ 3)
console.log(result) // 1

//Operador NOT
result = (~3)
console.log(result) // -4

// Operador left shift
result = (2 << 1)
console.log(result) // 4 

// Operador right shift
result = (2 >> 1)
console.log(result) // 1